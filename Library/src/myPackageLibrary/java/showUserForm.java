    public void showuser(){
        ArrayList<register>  list=(ArrayList<register>) getuser();
         DefaultTableModel  model=(DefaultTableModel)showusertbl.getModel();
         Object[] row=new Object[5];
          for(int i=0; i<list.size(); i++){
              row[0]=list.get(i).getFullname();
              row[1]=list.get(i).getUsername();
              row[2]=list.get(i).getPassword();
              row[3]=list.get(i).getPhone();
              row[4]=list.get(i).getEmail();
              model.addRow(row);
              
          }
    }
    
    public final void showbook(){
        ArrayList<borrowBook>  list=(ArrayList<borrowBook>) getBorrowbooks();
         DefaultTableModel  model=(DefaultTableModel)showbooktbl.getModel();
         Object[] row=new Object[4];
          for(int i=0; i<list.size(); i++){
              row[0]=list.get(i).getBookName();
              row[1]=list.get(i).getAuther();
              row[2]=list.get(i).getGenre();
              row[3]=list.get(i).getUsername();
              model.addRow(row);
              
          }
    }
